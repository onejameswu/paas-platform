COMMENT ON COLUMN SYS_ROLE.STATUS IS '状态 (1=启用 0=禁用)';
COMMENT ON COLUMN SYS_ROLE.CREATE_TIME IS '创建时间';
COMMENT ON COLUMN SYS_ROLE.UPDATE_TIME IS '更新时间';

